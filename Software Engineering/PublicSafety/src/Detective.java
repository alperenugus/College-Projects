
public class Detective {
	private String name;
	private int lastBadgeNum;
	
	
	public Detective(String name, int lastBadgeNum) {
		super();
		this.name = name;
		this.lastBadgeNum = lastBadgeNum;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLastBadgeNum() {
		return lastBadgeNum;
	}
	public void setLastBadgeNum(int lastBadgeNum) {
		this.lastBadgeNum = lastBadgeNum;
	}
	
	
}
